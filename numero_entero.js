function getFloor(numero) {


    return Math.floor(numero);
 }



/**El método Math.Floor devuelve el máximo entero redondeado más bajo de la variable
 * en el caso de los negativos, devueve el siguiente (-44.5) devolvería el -45 ya que
 * matemáticamente hablando es más chico que el (-44.5).
 */



/**El método Math.trunc devuelve la parte entrea de cualquier número quitando la parte decimal 
 * sin importar si el argumento es un número positivo o negativo. Es decir no pasa como en 
 * Math.floor que redondea al más pequeño sino en éste caso quita los valores despues del punto
 * y deja el valor entero sin cambios.
 */



function getTrunc(numero) {

    return Math.trunc(numero);
    
}










